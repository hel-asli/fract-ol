#include "../fractol.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	if (!s1 || !s2)
		return (0);
	while ((*s1 || *s2) && *s1 == *s2 && n >= 0)
	{
		s1++;
		s2++;
		n--;
	}
	return (*s1 - *s2);
}
